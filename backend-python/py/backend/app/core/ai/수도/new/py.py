# AI 엔진 로직 (Stateless: 저장을 안 하고 매번 계산함)

# [Func 1] 게임 시작 (generate_scenario)
# 1. 랜덤으로 사건 유형 결정 (보이스피싱 type A / 정상유저 type B / 스미싱 type C ...)
# 2. 해당 유형에 맞는 "초기상황(Context)"과 "첫 대사" 생성
# 3. 반환: { 시나리오ID, 초기대사, 힌트(디버깅용) }

# [Func 2] 대화 진행 (chat_response)
# 1. 입력받은 "시나리오ID"로 현재 AI가 연기해야 할 페르소나(성격) 로딩
# 2. "대화내역(History)"을 읽어서 문맥 파악
# 3. 사용자 "질문"에 대해 페르소나에 맞춰 답변 생성
#    (ex: 보이스피싱범이면 당황하거나 화를 냄, 정상인이면 차근차근 설명함)
# 4. 반환: { AI답변 }

# [Func 3] 결과 판결 (evaluate_result)
# 1. "시나리오ID"의 정답(True/False) 확인
# 2. 사용자의 "선택(Button)"과 정답 비교
# 3. "대화내역"을 분석하여 사용자가 얼마나 꼼꼼히 탐문했는지 분석
# 4. 반환: { 정답여부, 획득점수, 상세해설(왜 보이스피싱이었는지), AI의 한마디 }