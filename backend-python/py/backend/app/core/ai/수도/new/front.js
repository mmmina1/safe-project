// [UI 구성]
// - 상황 연출 창 (AI 대사 표시)
// - 질문 입력 창 (플레이어가 탐문)
// - 판단 버튼들 (보이스피싱 / 정상 / 악성앱 등)

// [Flow 1: 게임 시작 (Start)]
// 1. "시뮬레이션 시작" 요청 (-> Spring -> Python)
// 2. 초기 상황/시나리오 수신 (ex: "노인이 헐레벌떡 들어온다")
// 3. 화면에 상황 연출 및 첫 대사 출력
// 4. 대화 기록(History) 초기화

// [Flow 2: 탐문 수사 (Chat Loop)]
// -- 플레이어가 질문을 입력할 때마다 반복 --
// 1. 플레이어 질문 입력 -> 전송 (시나리오정보 + 대화기록 + 질문)
// 2. AI 답변 수신 (ex: "아, 그냥 쓸 돈이라니까요!")
// 3. 화면에 답변 출력 및 대화 기록에 추가(Append)
// 4. 플레이어는 답변을 보고 "더 질문할지" or "지금 판단할지" 결정

// [Flow 3: 최종 판결 (Judge)]
// -- 판단 버튼을 눌렀을 때 --
// 1. 버튼 클릭 (ex: "보이스피싱 의심" 선택) -> 전송 (시나리오정보 + 대화기록 + 선택한값)
// 2. 결과 수신 (정답여부 + 해설 + 탐문 점수)
// 3. 결과 화면 출력 (성공/실패 이펙트)
// 4. "다음 손님 받기" 버튼 -> Flow 1로 이동